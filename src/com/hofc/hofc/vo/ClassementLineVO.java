package com.hofc.hofc.vo;

public class ClassementLineVO {
	private String nom;
	private int points;
	private int joue;
	private int gagne;
	private int nul;
	private int perdu;
	private int bp;
	private int bc;
	private int diff;
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getPoints() {
		return points;
	}
	public void setPoints(int points) {
		this.points = points;
	}
	public int getJoue() {
		return joue;
	}
	public void setJoue(int joue) {
		this.joue = joue;
	}
	public int getGagne() {
		return gagne;
	}
	public void setGagne(int gagne) {
		this.gagne = gagne;
	}
	public int getNul() {
		return nul;
	}
	public void setNul(int nul) {
		this.nul = nul;
	}
	public int getPerdu() {
		return perdu;
	}
	public void setPerdu(int perdu) {
		this.perdu = perdu;
	}
	public int getBp() {
		return bp;
	}
	public void setBp(int bp) {
		this.bp = bp;
	}
	public int getBc() {
		return bc;
	}
	public void setBc(int bc) {
		this.bc = bc;
	}
	public int getDiff() {
		return diff;
	}
	public void setDiff(int diff) {
		this.diff = diff;
	}
	
	
}
